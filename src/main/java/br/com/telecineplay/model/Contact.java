package br.com.telecineplay.model;

public class Contact {

	  private String name;
	  private int age;

	  public Contact() {}

	  public String getName() {
	    return name;
	  }

	  public Contact setName(String name) {
	    this.name = name;
	    return this;
	  }

	  public int getAge() {
	    return age;
	  }

	  public Contact setAge(int age) {
	    this.age = age;
	    return this;
	  }
	}

